#!/usr/bin/env node

const fs = require('node:fs');
const path = require('node:path');

const root = path.resolve(__dirname, '..');
const patchNotesDir = path.join(root, 'docs', 'patch-notes');
const outputFile = path.join(patchNotesDir, 'LATEST-INDEX.md');

function listPatchNotes() {
  const entries = fs.readdirSync(patchNotesDir, { withFileTypes: true });

  return entries
    .filter((entry) => entry.isFile() && entry.name.endsWith('.md'))
    .map((entry) => entry.name)
    .filter((name) => name !== 'LATEST-INDEX.md');
}

function splitPatchNotes(files) {
  const numbered = [];
  const other = [];

  for (const file of files) {
    const match = file.match(/^(\d+)-(.+)\.md$/);
    if (match) {
      numbered.push({
        file,
        number: Number(match[1]),
        slug: match[2].replace(/-/g, ' '),
      });
    } else {
      other.push(file);
    }
  }

  numbered.sort((a, b) => b.number - a.number);
  other.sort((a, b) => a.localeCompare(b));
  return { numbered, other };
}

function renderIndex(numbered, other) {
  const latest = numbered[0];

  const lines = [
    '# Patch Notes - Latest Index',
    '',
    'Auto-generated by `npm run docs:patch-notes-index`.',
    '',
    latest
      ? `Most recent numbered patch note: [${latest.file}](./${latest.file})`
      : 'No numbered patch notes found.',
    '',
    '## Numbered Patch Notes (Newest First)',
    '',
  ];

  if (numbered.length === 0) {
    lines.push('_None found._');
  } else {
    for (const item of numbered) {
      lines.push(`- [${item.file}](./${item.file}) - ${item.slug}`);
    }
  }

  lines.push('', '## Other Patch Note Files', '');

  if (other.length === 0) {
    lines.push('_None found._');
  } else {
    for (const file of other) {
      lines.push(`- [${file}](./${file})`);
    }
  }

  lines.push('');
  return lines.join('\n');
}

function main() {
  const files = listPatchNotes();
  const { numbered, other } = splitPatchNotes(files);
  const content = renderIndex(numbered, other);
  fs.writeFileSync(outputFile, content, 'utf8');
  process.stdout.write(`Generated ${path.relative(root, outputFile)}\n`);
}

main();
